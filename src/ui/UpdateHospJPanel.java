    private void btnUpdateHospActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateHospActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model= (DefaultTableModel) tblHosp.getModel();
        if(tblHosp.getSelectedRowCount() == 1){            
            String hid=txtHospId.getText();
            
            String hospName=txtHospName.getText();
            
            String commId=txtCommId.getText();
            int selectedRowIndex = tblHosp.getSelectedRow();
        
        Hospital hp = hosp.getHosp().get(selectedRowIndex);
        
        hp.setHospitalId(hid);
        hp.setHospitalName(hospName);
        hp.setCommunityId(commId);
        }
        
        JOptionPane.showMessageDialog(this, "The record has been updated successfully");
        populateTable();
    }//GEN-LAST:event_btnUpdateHospActionPerformed

    private void tblHospMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHospMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = tblHosp.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select an employee profile to Update");
            return;
        }
       DefaultTableModel model= (DefaultTableModel) tblHosp.getModel();
       Hospital selectedHospital = (Hospital) model.getValueAt(selectedRowIndex, 0);
       txtHospId.setText(String.valueOf(selectedHospital.getHospitalId()));
        txtHospName.setText(selectedHospital.getHospitalName());
        txtCommId.setText(selectedHospital.getCommunityId());
    }//GEN-LAST:event_tblHospMouseClicked

      private void populateTable() {
        DefaultTableModel model= (DefaultTableModel) tblHosp.getModel();
        model.setRowCount(0);
        
        for(Hospital hp : hosp.getHosp()){
            Object[] row = new Object[3];
            row[0] = hp;
            row[1] = hp.getHospitalName();
            row[2] = hp.getCommunityId();
            model.addRow(row);
            
        }
    }

