    private void boxHospitalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxHospitalsActionPerformed
        // TODO add your handling code here:
        
         Object item = this.boxHospitals.getSelectedItem();
        String cid = ((ComboItem)item).getValue();

        if(cid.length() == 0) {
            JOptionPane.showMessageDialog(this, "Hospital details can't be empty");
            return;
        }
        this.boxOrgs.removeAllItems();
        ArrayList<DoctorOrg> dorgs = new DoctorOrgService().getDoctoOrgsInHospital(cid);
        for(DoctorOrg hospital: dorgs) {
            System.out.println(hospital.getName()+hospital.getId());
            this.boxOrgs.addItem(new ComboItem(hospital.getName(), hospital.getId()));
        }
    }//GEN-LAST:event_boxHospitalsActionPerformed


