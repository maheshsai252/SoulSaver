    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Object item = this.boxHospitals.getSelectedItem();
        String cid = ((ComboItem)item).getValue();

        if(cid.length() == 0) {
            JOptionPane.showMessageDialog(this, "Hospital details can't be empty");
            return;
        }
        System.out.println(cid+"received");
        this.doc.setHospitalId(cid);
        Object item2 = this.boxOrgs.getSelectedItem();
        String cid2 = ((ComboItem)item2).getValue();

        if(cid2.length() == 0) {
            JOptionPane.showMessageDialog(this, "Doctor org details can't be empty");
            return;
        }
        this.doc.setDoctororg_id(cid2);
        String msg = new DoctorService().update(doc);
        JOptionPane.showMessageDialog(this, msg);
    }//GEN-LAST:event_btnUpdateActionPerformed

