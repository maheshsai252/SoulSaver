    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int idx = this.tblEncounters.getSelectedRow();
        if(idx<0) {
            JOptionPane.showMessageDialog(this, "Not correct index");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) this.tblEncounters.getModel();

        Encounter v = (Encounter) model.getValueAt(idx, 1);
        if (v.getVitalSigns() == null) {
            JOptionPane.showMessageDialog(this, "Not correct vitals");
            return;
        }
        this.txtDiagnose.setText(v.getDiagnosis());
        this.lblage.setText(String.valueOf(v.getVitalSigns().getAgeGroup()));
        this.lblBP.setText(String.valueOf(v.getVitalSigns().getBloodPressure()));
        this.lblHeartRate.setText(String.valueOf(v.getVitalSigns().getHeartRate()));
        this.lblRespiratoryRate.setText(String.valueOf(v.getVitalSigns().getRespiratoryRate()));
        this.lblWeight.setText(String.valueOf(v.getVitalSigns().getWeightInKgs()));
    }//GEN-LAST:event_jButton1ActionPerformed


