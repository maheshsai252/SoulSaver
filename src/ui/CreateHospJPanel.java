    private String getCommunityByName(String name) {
        String cid="";
        if(this.coms!=null) {
            for(Community com: coms){
                if(com.getCommunityName().equals(name)) {
                    return com.getCommunityID();
                }
        }
        }
        return cid;
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
    String hospitalId = txtHospId.getText();
    if(hospitalId.isEmpty()){
            JOptionPane.showMessageDialog(this, "Hospital ID should not empty");
            return;
        }
    String hospitalName = txtHospName.getText();
    if(hospitalName.isEmpty()){
            JOptionPane.showMessageDialog(this, "Hospital Name should not empty");
            return;
        }
    String communityID = txtCommId.getText();
    
    
    Hospital hp = hosp.addNewHosp();
    hp.setHospitalId(hospitalId);
    hp.setHospitalName(hospitalName);
    hp.setCommunityId(communityID);
    
    JOptionPane.showMessageDialog(this,"New Hospital Added to List");
    
    txtHospId.setText("");
    txtHospName.setText("");
    txtCommId.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed


