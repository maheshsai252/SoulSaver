private void populateTable() {
           DefaultTableModel m=(DefaultTableModel) tblViewEncounter.getModel();
           m.setRowCount(0);
            if(ds.getEnc()!=null)  {
           for(Encounter  v:ds.getEnc())
           {            
                 Object[] row=new Object[4];
               row[0] = v;
                row[1]=v.getDid();
               row[2]=v.getVitalId();
               row[3]=v.getDate();
               m.addRow(row);
           }
           } 
            
    }
