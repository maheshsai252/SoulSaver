    private void btnSaveVitals1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveVitals1ActionPerformed
        // TODO add your handling code here:

        int SelRowInd=tblViewEncounter.getSelectedRow();
        if(SelRowInd<0)
        {
            JOptionPane.showMessageDialog(this, "Please select a row to view");
            return;
        }

        DefaultTableModel m=(DefaultTableModel) tblViewEncounter.getModel();
        VitalSigns SelPat=(VitalSigns)m.getValueAt(SelRowInd, 0);

        txtVitalId.setText(String.valueOf(SelPat.getPatientId()));
        txtPatientId.setText(String.valueOf(SelPat.getPatientId()));
        txtBP.setText(String.valueOf(SelPat.getBloodPressure()));
        txtTemp.setText(String.valueOf(SelPat.getBodyTemperature()));
        txtRespRate.setText(String.valueOf(SelPat.getRespirationRate()));

    }//GEN-LAST:event_btnSaveVitals1ActionPerformed


